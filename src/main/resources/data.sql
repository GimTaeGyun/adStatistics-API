-- 재시작 시 테이블 초기화

DROP TABLE AD_STATISTICS;

-- 날짜, 시간 복합키 지정
CREATE TABLE AD_STATISTICS 
(
	COL_DATE VARCHAR(10) NOT NULL, 
	COL_HOUR VARCHAR(2) NOT NULL,  
	REQ_CNT BIGINT(20), 
	RES_CNT BIGINT(20),
	CTR_CNT BIGINT(20),
	PRIMARY KEY(COL_DATE, COL_HOUR)
);

